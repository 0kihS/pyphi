def trivial():
    """Single-node network with a self-loop."""

    trivial = Network(np.array([[1], [1]]), (1, ), (1, ),
                      connectivity_matrix=np.array([[1]]))

    return Subsystem(range(trivial.size),
                     trivial.current_state,
                     trivial.past_state,
                     trivial)


